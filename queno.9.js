// Q.9. Convert the following if else if construct into switch case


let choice=3;

switch(choice)
{
    case 1:
            console.log("Good")
        break;

    case 2:
            console.log('better')
        break;

    case 3:
            console.log("best")
        break;
    default:
            console.log(`invalid choice`)
}


// output is :- best